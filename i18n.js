const I18n = {
  currentLanguage: 'en',
  
  init(callback) {
    if (chrome?.storage?.local) {
      chrome.storage.local.get(['settings'], (result) => {
        if (result.settings && result.settings.language) {
          this.currentLanguage = result.settings.language;
        } else {
          this.currentLanguage = this.detectBrowserLanguage();
        }
        if (callback) callback();
      });
    } else {
      this.currentLanguage = this.detectBrowserLanguage();
      if (callback) callback();
    }
  },
  
  detectBrowserLanguage() {
    const lang = navigator.language || navigator.userLanguage || 'en';
    if (lang.startsWith('zh')) {
      return 'zh_CN';
    }
    return 'en';
  },
  
  setLanguage(lang) {
    this.currentLanguage = lang;
  },
  
  get(key, substitutions) {
    const lang = translations[this.currentLanguage] || translations.en;
    let message = lang[key] || translations.en[key] || key;
    
    if (substitutions) {
      if (Array.isArray(substitutions)) {
        substitutions.forEach((sub, i) => {
          message = message.replace(new RegExp(`\\$COUNT\\$`, 'g'), sub);
          message = message.replace(new RegExp(`\\$NUMBER\\$`, 'g'), sub);
          message = message.replace(new RegExp(`\\$ERROR\\$`, 'g'), sub);
          message = message.replace(new RegExp(`\\$TAG\\$`, 'g'), sub);
        });
      } else {
        message = message.replace(/\$COUNT\$/g, substitutions);
        message = message.replace(/\$NUMBER\$/g, substitutions);
        message = message.replace(/\$ERROR\$/g, substitutions);
        message = message.replace(/\$TAG\$/g, substitutions);
      }
    }
    
    return message;
  },
  
  applyToDocument() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      el.textContent = this.get(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
      const key = el.getAttribute('data-i18n-placeholder');
      el.placeholder = this.get(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
      const key = el.getAttribute('data-i18n-title');
      el.title = this.get(key);
    });
  }
};
